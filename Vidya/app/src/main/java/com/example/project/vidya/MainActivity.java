    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            // getItem is called to instantiate the fragment for the given page.
            // Return a PlaceholderFragment (defined as a static inner class below).


            Log.e("Pos", Integer.toString(position));
            switch (position){

                case 0:
                    return AboutMeFragment.newInstance();

                case 2:
                    return Skill.newInstance();
                case 3:
                    return DreamProject.newInstance();
                case 4:
                    return WhyAnd.newInstance();
                case 5:
                    return Ach.newInstance();
                case 1:
                    return EducationFragment.newInstance();





            }
            return null;
        }

        @Override
        public int getCount() {
            // Show 6 total pages.
            return 6;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "About Me";
                case 1:
                    return "Educational Qualifications";
                case 2:
                    return "Skills";
                case 3:
                    return "Dream Project";
                case 4:
                    return "Why Android";
                case 5:
                    return "Achievements";
            }
            return null;
        }
    }
